<h3>Our objectives: </h3>
<ul>
<li>To help parents and primary carers to form a more secure attachment with their baby through psychotherapeutic intervention.</li>

<li>To promote the importance of early attachment and the importance of investment in very early relationships to improve life outcomes for children. In particular DorPIP will raise awareness amongst professionals, commissioners, investors, and policy makers.</li>

<li>To train other professionals in supporting secure attachments between infants and their primary carers, in the identification of attachment difficulties and in the methods used to facilitate better attachments.</li>

